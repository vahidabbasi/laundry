CREATE SEQUENCE SEQU;

CREATE TABLE BOOKING
(
    BOOKING_REFERENCE INTEGER GENERATED BY DEFAULT AS SEQUENCE SEQU PRIMARY KEY,
    HOUSEHOLDER_ID    INTEGER,
    LAUNDRY_ID        INTEGER,
    STARTED_AT        TIMESTAMP,
    ENDED_AT          TIMESTAMP,
    CANCEL_BOOKING_AT TIMESTAMP,
    CONSTRAINT CHEAK_HOUSEHOLDER_ID CHECK (HOUSEHOLDER_ID >= 1 AND HOUSEHOLDER_ID <= 20),
    CONSTRAINT CHECK_LAUNDRY_ID CHECK (LAUNDRY_ID >= 1 AND LAUNDRY_ID <= 2)
);
